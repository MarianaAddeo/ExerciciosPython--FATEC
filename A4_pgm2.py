# Escreva um programa que contenha duas variáveis: A = 12 e B = 4
# O programa deve exibir na tela quatro resultados:
#  soma A + B,
#  subtração A – B,
#  multiplicação A * B e 
#  divisão real A / B
#

A = 12
B = 4
R = A + B
print('Soma = ', R)
R = A - B
print('Subtração = ', R)
R = A * B
print('Multiplicação = ', R)
R = A / B
print('Divisão = ', R)
print('\n\nFim do Programa')
